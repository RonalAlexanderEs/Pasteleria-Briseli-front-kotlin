package pe.edu.idat.appborabora.view.activities

class Respuesta{
    var Exito: String? = null
    var Mensaje: String? = null

    fun getExito(): String? {
        return Exito
    }

    fun setExito(Exito: String?) {
        this.Exito = Exito
    }

    fun getMensaje(): String? {
        return Mensaje
    }

    fun setMensaje(Mensaje: String?) {
        this.Mensaje = Mensaje
    }

}
